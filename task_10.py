import math

# Запрашиваем у пользователя радиус и высоту цилиндра
radius = float(input("Введите радиус основания цилиндра в сантиметрах: "))
height = float(input("Введите высоту цилиндра в сантиметрах: "))

# Вычисляем объем цилиндра
volume = math.pi * (radius ** 2) * height

# Выводим результат
print(f"Объем цилиндра: {volume:.2f} куб.см")