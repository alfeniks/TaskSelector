force = float(input("Введите силу в ньютонах: "))
area = float(input("Введите площадь в квадратных метрах: "))

# Проверяем, что площадь не равна нулю, чтобы избежать деления на ноль
if area != 0:
    # Вычисляем давление
    pressure = force / area
    print(f"Давление: {pressure} паскалей")
else:
    print("Ошибка: площадь не может быть равна нулю.")